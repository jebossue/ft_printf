#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	unsigned int	i;

	i = 0;
	if (!*s1 || !*s2)
		return (((unsigned char)*s1 - (unsigned char)*s2));
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return (((unsigned char)s1[i] - (unsigned char)s2[i]));
		i++;
	}
	return (0);
}
